# 1. volume kubus
print("1. menghitung volume kubus")
s = int(input('sisi:'))
v = s*s*s
print("jika sisi kubus adalah",s,"cm,","maka volume kubus adalah",v,"cm3.")

# 2. volume balok
print("2. menghitung volume balok")
p = int(input('panjang balok:'))
l = int(input('lebar balok:'))
t = int(input('tinggi balok:'))
v = p*l*t
print("jika panjang balok",p,"cm,","lebar balok",l,"cm","tinggi balok",t,"cm","maka volume balok adalah",v,"cm3.")
